import 'dart:io';

import 'package:wallet_hunter/features/registration/domain/entities/member_entity.dart';

class MemberModel extends MemberEntity {
  MemberModel({
    required super.firstName,
    required super.middleName,
    required super.lastName,
    required super.age,
    required super.gender,
    required super.maritalStatus,
    required super.occupation,
    required super.qualification,
    required super.birthDate,
    required super.bloodGroup,
    required super.natureOfDuties,
    required super.emailAddress,
    required super.phoneNumber,
    required super.alterNumber,
    required super.landlineNumber,
    required super.socialMediaUrl,
    required super.flatNumber,
    required super.buildingName,
    required super.streetName,
    required super.landMark,
    required super.city,
    required super.district,
    required super.state,
    required super.nativeCity,
    required super.nativeState,
    required super.country,
    required super.pincode,
    File? photo,
    String? photoUrl,
  }) : super(photo: photo, photoUrl: photoUrl);

  factory MemberModel.fromJson(Map<String, dynamic> json) => MemberModel(
    firstName: json['firstName'] ?? '',
    middleName: json['middleName'] ?? '',
    lastName: json['lastName'] ?? '',
    age: json['age'] ?? 0,
    gender: json['gender'] ?? '',
    maritalStatus: json['maritalStatus'] ?? '',
    occupation: json['occupation'] ?? '',
    qualification: json['qualification'] ?? '',
    birthDate: json['birthDate'] ?? '',
    bloodGroup: json['bloodGroup'] ?? '',
    natureOfDuties: json['natureOfDuties'] ?? '',
    emailAddress: json['emailAddress'] ?? '',
    phoneNumber: json['phoneNumber'] ?? '',
    alterNumber: json['alterNumber'] ?? '',
    landlineNumber: json['landlineNumber'] ?? '',
    socialMediaUrl: json['socialMediaUrl'] ?? '',
    flatNumber: json['flatNumber'] ?? '',
    buildingName: json['buildingName'] ?? '',
    streetName: json['streetName'] ?? '',
    landMark: json['landMark'] ?? '',
    city: json['city'] ?? '',
    district: json['district'] ?? '',
    state: json['state'] ?? '',
    nativeCity: json['nativeCity'] ?? '',
    nativeState: json['nativeState'] ?? '',
    country: json['country'] ?? '',
    pincode: json['pincode'] ?? '',
    photoUrl: json['photoUrl'],
  );

  Map<String, dynamic> toJson() => {
    'firstName': firstName,
    'middleName': middleName,
    'lastName': lastName,
    'age': age,
    'gender': gender,
    'maritalStatus': maritalStatus,
    'occupation': occupation,
    'qualification': qualification,
    'birthDate': birthDate,
    'bloodGroup': bloodGroup,
    'natureOfDuties': natureOfDuties,
    'emailAddress': emailAddress,
    'phoneNumber': phoneNumber,
    'alterNumber': alterNumber,
    'landlineNumber': landlineNumber,
    'socialMediaUrl': socialMediaUrl,
    'flatNumber': flatNumber,
    'buildingName': buildingName,
    'streetName': streetName,
    'landMark': landMark,
    'city': city,
    'district': district,
    'state': state,
    'nativeCity': nativeCity,
    'nativeState': nativeState,
    'country': country,
    'pincode': pincode,
    'photoUrl': photoUrl,
  };
}
